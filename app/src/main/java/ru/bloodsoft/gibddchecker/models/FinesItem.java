package ru.bloodsoft.gibddchecker.models;

public class FinesItem {
    private String date;
    private String koap;
    private String division;
    private String summa;
    private String reqToken;
    private String numPost;
    private String regNumber;
    private String fullAddr;
    private String coordinates;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getKoap() {
        return koap;
    }

    public void setKoap(String koap) {
        this.koap = koap;
    }

    public String getDivision() {
        return division;
    }

    public void setDivision(String division) {
        this.division = division;
    }

    public String getSumma() {
        return summa;
    }

    public void setSumma(String summa) {
        this.summa = summa;
    }

    public String getReqToken() {
        return reqToken;
    }

    public void setReqToken(String reqToken) {
        this.reqToken = reqToken;
    }

    public String getNumPost() {
        return numPost;
    }

    public void setNumPost(String numPost) {
        this.numPost = numPost;
    }

    public String getRegNumber() {
        return regNumber;
    }

    public void setRegNumber(String regNumber) {
        this.regNumber = regNumber;
    }

    public String getFullAddr() {
        return fullAddr;
    }

    public void setFullAddr(String fullAddr) {
        this.fullAddr = fullAddr;
    }

    public String getCoordinates() {
        return coordinates;
    }

    public void setCoordinates(String coordinates) {
        this.coordinates = coordinates;
    }
}